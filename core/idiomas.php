// Función helper para obtener traducciones
function obtenerTraduccion($clave, $idioma = null) {
    global $traducciones;
    
    // Si no se especifica idioma, usar el de la sesión
    if ($idioma === null) {
        $idioma = $_SESSION['user']['idioma'] ?? 'español';
    }
    
    // Validar que el idioma existe
    if (!isset($traducciones[$idioma])) {
        $idioma = 'español'; // Idioma por defecto
    }
    
    // Devolver la traducción o la clave si no existe
    return $traducciones[$idioma][$clave] ?? $clave;
}

// Alias corto para facilitar su uso
function t($clave, $idioma = null) {
    return obtenerTraduccion($clave, $idioma);
}
